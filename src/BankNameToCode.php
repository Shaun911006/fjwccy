    public static function getBankCodeByName($bankName)
    {
        $a = strripos($bankName, '银行'); //银行首次出现的位置
        if ($a !== false) {
            $bankName = substr($bankName, 0, $a) . '银行';
            switch ($bankName) {
                case '中国银行':
                    return 'BKCH';
                case '中国进出口银行':
                    return 'EIBC';
                case '中国农业发展银行':
                    return 'ADBN';
                case '中国信托银行':
                    return 'CTCB';
            }
            $bankName = str_replace('中国', '', $bankName);
        } elseif (strripos($bankName, '邮政储蓄') !== false) {
            $bankName = '邮政储蓄';
        } elseif (strripos($bankName, '意大利联合信贷') !== false) {
            $bankName = '意大利联合信贷';
        } else {
            $bankName = '其他';
        }
        return self::$arr[$bankName] ?? '9999';
    }
